package day02;

public class TestVariable {
    public static void main(String[] args) {
        int a = 10;
        a = 20;
        System.out.println(a);

        double b = 3.14;
        System.out.println(b);


        char c = 'a';
        System.out.println(c);

        boolean d = true;
        System.out.println(d);

        String s = "abc";
        System.out.println(s);

        int x;
        x = 100;
        System.out.println(x);

    }
}
